package department

import (
	"context"
	"net/http"
	"strconv"

	"main/internal/entity"
	department "main/internal/usecase/department"

	"github.com/gin-gonic/gin"
)

type Controller struct {
	useCase *department.UseCase
}

func NewController(useCase *department.UseCase) *Controller {
	return &Controller{useCase: useCase}
}

func (ac Controller) AdminCreateDepartment(c *gin.Context) {
	var data department.Create

	if err := c.ShouldBind(&data); err != nil {
		c.JSON(400, gin.H{"error": err.Error()})
		return
	}

	authHeader := c.GetHeader("Authorization")
	if authHeader == "" {
		c.JSON(401, gin.H{"error": "Unauthorized"})
		return
	}

	ctx := context.Background()
	departmentId, err := ac.useCase.Create(ctx, data, authHeader)
	if err != nil {
		c.JSON(500, gin.H{"error": err.Error()})
		return
	}

	c.JSON(200, gin.H{"message": "ok!", "id": departmentId})
}

func (ac Controller) AdminDeleteDepartment(c *gin.Context) {
	idParam := c.Param("id")
	id, err := strconv.ParseInt(idParam, 10, 64)
	if err != nil {
		c.JSON(http.StatusBadRequest, gin.H{"error": "invalid id"})
		return
	}

	authHeader := c.GetHeader("Authorization")
	if authHeader == "" {
		c.JSON(401, gin.H{"error": "Unauthorized"})
		return
	}

	ctx := context.Background()
	if err := ac.useCase.Delete(ctx, id, authHeader); err != nil {
		c.JSON(500, gin.H{"error": err.Error()})
		return
	}

	c.JSON(200, gin.H{"message": "ok!"})
}

